# Ініціалізація констант
PI = 3.14
RADIUS = 10
# Виведення початкових значень
print("Початкове значення PI:", PI)
print("Початкове значення RADIUS:", RADIUS)
# Спроба змінити значення константи
print("Спроба змінити значення PI...")
PI = 3.14159  # Python дозволяє це зробити, але це неправильно
# Виведення "нового" значення
print("Новий (змінений) PI:", PI)
# Пояснення
print("Змінювати константи не можна! Це суперечить правилам програмування і може призвести до помилок у коді.")

